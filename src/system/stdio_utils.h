/* popen */
#define POPEN_REPORT(STREAM, COMMAND, MODE, FAILURE)			\
do {									\
	STREAM = popen(COMMAND, MODE);					\
	if (STREAM == NULL) {						\
		*(FAILURE) = FAILURE_REASON_3("popen",			\
					      "fork(2) failure",	\
					      "pipe(2) failure",	\
					      "failed to allocate "	\
					      "sufficient memory");	\
		return false;						\
	}								\
} while (0)



/* pclose */
#define PCLOSE_REPORT(STREAM, FAILURE)					\
do {									\
	if (pclose(STREAM) == -1) {					\
		*(FAILURE) = FAILURE_REASON_3("pclose",			\
					      "'stream' is not "	\
					      "associated with a "	\
					      "\"popened\" command",	\
					      "'stream' is already "	\
					      "\"pclosed\"",		\
					      "wait4(2) returned an "	\
					      "error");			\
		return false;						\
	}								\
} while (0)

